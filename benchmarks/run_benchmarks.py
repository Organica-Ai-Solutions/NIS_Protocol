async def run_benchmarks(quick: bool = False, verbose: bool = True):
    """Run the benchmark suite"""
    
    print_banner()
    print_comparison_table()
    
    print("\nüöÄ Starting NIS Protocol Benchmarks...")
    print("   Make sure the backend is running: ./start.sh")
    print()
    
    # Check if server is running
    import aiohttp
    try:
        async with aiohttp.ClientSession() as session:
            async with session.get("http://localhost:8000/health", timeout=aiohttp.ClientTimeout(total=5)) as resp:
                if resp.status != 200:
                    print("‚ùå Backend not responding. Please start with ./start.sh")
                    return
                print("‚úÖ Backend is running\n")
    except Exception as e:
        print(f"‚ùå Cannot connect to backend: {e}")
        print("   Please start the backend with: ./start.sh")
        return
    
    # Run benchmarks
    suite = NISBenchmarkSuite()
    result = await suite.run_all(verbose=verbose)
    
    # Print final verdict
    print("\n" + "=" * 60)
    print("FINAL VERDICT")
    print("=" * 60)
    
    if result.overall_score >= 90:
        verdict = "üèÜ EXCELLENT - Production Ready"
    elif result.overall_score >= 75:
        verdict = "‚úÖ GOOD - Minor improvements needed"
    elif result.overall_score >= 50:
        verdict = "‚ö†Ô∏è FAIR - Significant improvements needed"
    else:
        verdict = "‚ùå NEEDS WORK - Major issues detected"
    
    print(f"\n{verdict}")
    print(f"Score: {result.overall_score}%")
    print(f"Cost Efficiency: ${result.total_cost_usd:.4f} for {result.total_tests} tests")
    
    # Value proposition
    print("\nüìä VALUE PROPOSITION:")
    print(f"   ‚Ä¢ Orchestration overhead: {result.avg_latency_ms:.0f}ms avg")
    print(f"   ‚Ä¢ Multi-agent coordination: {'‚úÖ Working' if result.passed_tests > result.total_tests * 0.7 else '‚ö†Ô∏è Needs tuning'}")
    print(f"   ‚Ä¢ Memory retention: {'‚úÖ Working' if any(r.passed for r in result.results_by_category.get('multi_turn', [])) else '‚ö†Ô∏è Check memory system'}")
    
    return result


def main():
    parser = argparse.ArgumentParser(description="NIS Protocol Benchmark Suite")
    parser.add_argument("--quick", action="store_true", help="Run quick subset of tests")
    parser.add_argument("--verbose", action="store_true", default=True, help="Verbose output")
    parser.add_argument("--quiet", action="store_true", help="Minimal output")
    
    args = parser.parse_args()
    
    asyncio.run(run_benchmarks(
        quick=args.quick,
        verbose=not args.quiet
    ))


if __name__ == "__main__":
    main()
